◊section{Выделение и мониторинг лесов высокой природоохранной ценности}

Леса высокой природоохранной ценности (ЛВПЦ) — это леса, обладающие особо высокой экологической и социальной ценностью или имеющие ключевое значение для сохранения биоразнообразия. В стандартах FSC сохранению этих лесов посвящен Принцип 9 «Сохранение лесов, имеющих высокую природоохранную ценность».

К ним относятся редкие и уникальные для данного региона типы леса и ландшафта, участки с редкими видами флоры и фауны, леса с особо богатым биоразнообразием, участки, выполняющие средозащитные и ресурсоохранные функции (водоохранные, противоэрозионные и т.д.), а также участки, имеющие важную эстетическую, культурную и иную социальную ценность.

В ЛВПЦ ценность древесины оказывается второстепенной по сравнению с их значимостью для сохранения биоразнообразия, поддержания экологического равновесия и/или обеспечения потребностей местного населения. Режим охраны ЛВПЦ должен быть направлен на сохранение или увеличение их высокой природоохранной ценности.
Сертифицированные лесхозы должны охранять и сохранять леса высокой природоохранной ценности (ЛВПЦ) на своей территории. Необходимо обеспечить широкое участие заинтересованных сторон в процессах определения и выявления ЛВПЦ. Это необходимо для того, чтобы на основе множественных экспертиз и многостороннего опыта при принятии данного субъективного решения учитывать по возможности широкий круг интересов.

Лесхозы должны также иметь:

◊ul{
◊li{базовые документы о ЛВПЦ;}
◊li{списки ЛВПЦ для своей территории;}
◊li{карты ЛВПЦ;}
◊li{описание природной ценности ЛВПЦ;}
◊li{лесотипологические характеристики ЛВПЦ;}
◊li{план и результаты мониторинга ЛВПЦ.}
}

◊em{Важно}: данная информация является открытой и общественно доступной.

◊boxed{◊strong{Если вы хотите сохранить ценный участок леса.} Как заинтересованная сторона, вы можете убедиться, выделены ли все категории ЛВПЦ у того или иного лесхоза. Ознакомьтесь с информацией на интернет-сайте учреждения. Если там она не размещена, напишите запрос в учреждение с просьбой предоставить такую информацию. Вам не имеют права отказать в предоставлении данных о ЛВПЦ. Любой отказ или непредоставление информации является нарушением сразу нескольких критериев FSC. В случае отказа сообщите об этом соответствующей аудиторской компании, с приложением вашего письма и письма-ответа, в случае их наличия. После того, как вам предоставили информацию, внимательно изучите ее. Если вы располагаете информацией о каких-либо природных ценностях (места обитания охраняемых видов, места расположения редких биотопов, места, важные для местного населения и т.п.), сообщите об этом лесхозу, предложите включить данные участки в ЛВПЦ и установить соответствующий необходимый для них режим использования. При этом вы должны четко обосновать причины отнесения тех или иных территорий или участков леса к той или иной категории ЛВПЦ ◊em{[Практика охраны природы: практическое руководство для хранителей / сост. А.В. Абрамчук. Брест: Альтернатива, 2016. 152 с.]}.}

