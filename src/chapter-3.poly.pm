◊section{Выделение репрезентативных участков}

В стандартах FSC существует понятие репрезентативных участков, которые должны занимать не менее 5% площади и оставаться нетронутыми навсегда. Там не проводят рубки. 

◊em{Если участок пока не выделен, важно знать}: выделение происходит при общественном участии, и на выбор репрезентативных участков можно повлиять. Можно выслать ГПУ или ГЛХУ свои предложения по выделению конкретных участков в качестве репрезентативных.

◊boxed{
◊strong{Репрезентативные участки экосистем} — это участки, дающие объективное представление о естественных экосистемах рассматриваемой территории и представляющие собой резерваты дикой природы данной местности.

Это эталонные участки экосистем, предназначенные для мониторинга природных процессов и являющиеся модельными для данной территории. Эти участки предназначены для сохранения или восстановления жизнеспособных примеров природных экосистем, существующих в этом географическом регионе. Ради их сохранения ограничивается хозяйственная деятельность, за исключением случаев, когда проведение хозяйственных мероприятий направлено на их сохранение или восстановление.

Репрезентативные участки имеют строгий режим охраны. На них запрещены любые виды лесохозяйственной деятельности. 

Они должны включать все типы экосистем и ландшафтов, встречающихся на данной территории, то есть быть репрезентативными. Репрезентативные участки должны служить базой для научных исследований естественных процессов в экосистемах.

По возможности они должны быть функционально связаны между собой, чтобы обеспечивать сохранение биоразнообразия и поддерживать экологические функции экосистем. 
}

◊figure["img/24.jpg"]{Примеры репрезентативных участков. Фото Георгия Козулько}

FSC определяет следующие критерии в отношении репрезентативных участков: 

◊ul{
◊li{6.4.1. Репрезентативные участки существующих экосистем должны быть определены в процессе консультаций с научными учреждениями, государственными органами и неправительственными организациями, а также другими заинтересованными сторонами.}
◊li{6.4.2. Выявленные участки должны быть обозначены на картах и на местности и сохранены в своем естественном состоянии.}
◊li{6.4.3. Предприятие должно сохранять выделенные репрезентативные участки существующих экосистем по крайней мере на 5% лесной площади.}
◊li{6.4.5. На сохраняемых репрезентативных участках не должны осуществляться никакие рубки, за исключением случаев, когда это требуется для сохранения или увеличения сохраняемых ценностей и указано в письменном виде в правилах защитного участка.}
◊li{6.4.6. На защитных участках не должны сохраняться имеющиеся дренажные системы, за исключением случаев, когда их сохранение требуется для защиты сохраняемых ценностей согласно официальным правилам защиты или для направления воды с прилегающих земель.}
}

Таким образом, сертифицированные лесхозы должны охранять и сохранять репрезентативные участки (РУ) на своей территории.

Лесхозы должны также иметь:

◊ol{
◊li{базовые документы о репрезентативных участках;}
◊li{списки РУ для своей территории;}
◊li{карты РУ;}
◊li{лесотипологические характеристики РУ;}
◊li{план и результаты мониторинга РУ.}
}

◊em{Важно}: данная информация является открытой и общественно доступной.

