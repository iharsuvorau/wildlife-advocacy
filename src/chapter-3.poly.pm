◊heading{Примеры и пояснения относительно некоторых критериев FSC}

Ниже приведены некоторые пояснения и примеры относительно тех критериев, по которым достаточно часто выявляются несоответствия (нарушения) при проведении сертификации FSC, а именно: требования по сохранению элементов биоразнообразия при проведении рубок, выделению репрезентативных участков, выделению и охране мест обитания/произрастания редких и исчезающих видов, выделению и мониторингу лесов высокой природоохранной ценности и информированию общественности о результатах деятельности держателя сертификата.

◊section{Требования по сохранению элементов биоразнообразия при проведении рубок}

Уровень разнообразия животного и растительного мира на том или ином участке территории определяется прежде всего разнообразием условий местообитания, биотопов. К таким структурным объектам и комплексам относятся:

◊ul{
◊li{типы и подтипы растительности, лесные формации или группы формаций (мелколесья и кустарники, смешанные леса, широколиственные леса, сосновые, березовые леса и т.д., болота верховые, переходные, низинные, луга материковые или пойменные и др.); }
◊li{возрастные состояния лесов (молодняки, средневозрастные или высоковозрастные леса);}
◊li{водные объекты (водоемы, реки, каналы, родники);}
◊li{экотоны (опушки, берега рек и озер, переходные зоны от лесов к болотам и т.д.); }
◊li{отдельные геоморфологические образования (дюны, обрывы, отмели, поймы, крутые склоны);}
◊li{открытые участки среди леса (поляны, вырубки);}
◊li{отдельные внутриценотические элементы (сухие, одиночные, дуплистые деревья, пни, валежник, валуны и кучи камней, обнажения минерального грунта, муравейники, норы и т.д.);}
◊li{особенности пространственной структуры древостоев, «окна» в пологе, крупные деревья, наличие и состав примеси лиственных или хвойных, присутствие подлеска.}
}

◊boxed{
◊strong{Объекты, важные для сохранения биоразнообразия}, — это:

◊ul{
◊li{редкие лесные формации (дубравы, липняки, кленовники, ильмовники, ясенники),}
◊li{редкие типы леса (сосняки и березняки лишайниковые, приручейно-травяные, ельники осоковые и осоково-сфагновые и т.д.),}
◊li{островные местообитания за пределами ареала (ели, граба, ольхи серой),}
◊li{высоковозрастные насаждения,}
◊li{разновозрастные насаждения,}
◊li{многоярусные насаждения,}
◊li{насаждения, в составе которых более 4 пород,}
◊li{насаждения, в составе которых 20% и более осины с D>15 см,}
◊li{сосняки, многократно пройденные низовыми пожарами,}
◊li{места обитания редких и охраняемых видов флоры и фауны,}
◊li{леса, не затронутые лесохозяйственной или иной деятельностью,}
◊li{леса, граничащие с открытыми болотами, }
◊li{леса, граничащие с водотоками или водоемами,}
◊li{леса в пойме и граничащие с поймой, }
◊li{заболоченные бессточные участки до 5 га среди суходолов,}
◊li{островные леса до 5 га среди сельхозугодий,}
◊li{леса на минеральных островах до 5 га среди болот,}
◊li{леса на минеральных островах среди рек и озер,}
◊li{леса в долинах малых рек,}
◊li{леса, сопряженные с миграционными путями животных, }
◊li{участки лесов, граничащие с местами произрастания или обитания охраняемых и редких видов флоры и фауны (являющиеся потенциальными местами обитания для данных видов),}
◊li{перестойные деревья,}
◊li{деревья выдающихся размеров, с дуплами, редких форм, }
◊li{богатый по видовому составу, имеющий кормовое значение подлесок,}
◊li{покрытие ягодными кустарничками более 25% выдела,}
◊li{разнообразная эпифитная лихенофлора,}
◊li{крупномерный сухостой, высокие пни, сухостой с дуплами,}
◊li{обилие мертвой древесины разного возраста и стадий разложения,}
◊li{опушки,}
◊li{популяции редких и охраняемых видов флоры и фауны,}
◊li{родники,}
◊li{ручьи, }
◊li{истоки и верховья рек,}
◊li{скопления камней, валунов,}
◊li{овраги,}
◊li{карбонатная почва,}
◊li{выраженный мезо- и микрорельеф водоразделов (крутые склоны, западины, ложбины и т.п.),}
◊li{выраженный мезо- и микрорельеф речных долин (гривы, прирусловые валы),}
◊li{лесные поляны, }
◊li{луга всех типов более 0,5 га среди лесов,}
◊li{колониальные поселения птиц,}
◊li{объекты, имеющие культурно-историческую ценность и являющиеся специфическими местами обитания для растений и животных (аллеи, парки, фортификационные сооружения, }
◊li{участки брусчатых дорог, каменные фундаменты и т.п.).}
}}

Исходя из этого, при проведении рубок главного пользования (РГП) лесное хозяйство должно ориентироваться на следующие цели:

◊ul{
◊li{формирование разновозрастного насаждения со сложной пространственной структурой;}
◊li{формирование смешанного по составу древостоя; }
◊li{формирование смешанного по происхождению древостоя; }
◊li{сохранение биологически полезного подлеска (ягодных и медоносных кустарников), разнообразия кустарниковых пород, связанных с ними пищевыми и топическими связями видов, а также убежищ для лесных зверей и птиц; }
◊li{сохранение опушечной зоны для предотвращения экспансии нелесных видов, снижения отрицательного воздействия ветров, инсоляции;}
◊li{оставление малоценных фрагментов древостоя (неликвида, не достигших возраста спелости деревьев, групп подроста) для сохранения лесной среды, убежищ для лесной флоры и фауны, ускорения естественного заращивания вырубок, исключение непроизводительных трудозатрат, создание запаса будущего валежа;}
◊li{проведение РГП преимущественно в зимний период с целью предохранения от повреждений травяно-кустарничкового и мохово-лишайникового ярусов, снижения фактора беспокойства;}
◊li{сохранение отдельных вековых деревьев и деревьев, выдающихся по размерам, возрасту, величественному облику, поскольку они являются также местами обитания группы редких мхов, лишайников, насекомых, птиц, источниками уникальной дендрохронологической информации (10−15 шт/га); }
◊li{сохранение деревьев редких пород и причудливых форм для придания ландшафту определенного своеобразия; }
◊li{сохранение дуплистых деревьев (всех) в качестве мест обитания и привлечения животных-дуплогнездников, а также связанных с ними зоокомплексов (хищников, паразитов, сверхпаразитов и т.п.);}
◊li{сохранение высоких пней в качестве присад для хищных птиц, мест гнездований, создание запаса будущего валежа;}
◊li{сохранение отдельных крупномерных сухостойных деревьев в качестве специфического местообитания для большой группы растений, грибов, бактерий, животных, присад для хищных птиц (до 10 шт./га) в случае если они не несут угрозу и не опасны при проведении работ в лесу;}
}

